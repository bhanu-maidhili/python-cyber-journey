#Day 1: Understanding variables

# Storing text(string)
#  IP :  Internet protocol address (like physical street address but for computer on a network
target_ip = "192.168.1.50"

# Storing a whole number (integer)
#  Port : ( like specific apartment number inside a building) 
"""port 80 - unencrypted web traffic (HTTP)
port 443 - for secure web traffic (HTTP)
Port 22 - reserved for SSH (Secure Shell)"""
port = 22

# Storing a true/false value (Boolean)
is_active = True

# Printing the variables to the screen
print("Scanning IP:", target_ip)
print("Port Number:", port)
print("System Active:", is_active)
